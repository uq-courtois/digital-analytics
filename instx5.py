x[0] # will access the first element of list x
x[1] # will access the second element of list x
x[-1] # will access the last element of list x
x[:2] # will access the first two elements of list x, the : here means up to but not including the third element [2]
x[-2:] # will access the last two elements of list x, the : here means from the second last element [-2] until the end
